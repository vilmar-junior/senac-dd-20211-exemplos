-- CONSULTAS
-- VENDAS
SELECT
    CLIENTE.nome
	, CLIENTE.cpf
	, CLIENTE.telefone
	, VEICULO.modelo
	, VEICULO.placa
	, VENDA.dataVenda
	, VENDA.valorVenda
FROM
	CLIENTE
    INNER JOIN VENDA ON
    CLIENTE.IDCLIENTE = VENDA.IDCLIENTE
    INNER JOIN VEICULO ON
    VEICULO.IDVEICULO = VENDA.IDVEICULO;

-- FATURAMENTO
SELECT
	MONTH(DATAVENDA)
    , YEAR(DATAVENDA)
    , SUM(VALORVENDA)
FROM
	VENDA
GROUP BY
	MONTH(DATAVENDA)
    , YEAR(DATAVENDA)
ORDER BY 
	YEAR(DATAVENDA) DESC;